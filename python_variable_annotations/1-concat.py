#!/usr/bin/env python3
"""
Ce module contient une fonction qui concatene deux chaines de caracteres
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatene deux chaines de caracteres (str1 et str2)

    Args:

        str1 (str)
        str2 (str)

    Returns:
        str = str1 + str2
    """
    return str1 + str2
